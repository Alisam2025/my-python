# Removing element from a list using remove() method
motorcycles=['harley', 'honda', 'yamaha', 'suzuki', 'ducati']
print(motorcycles)
# Removing element using remove() method
motorcycles.remove('yamaha') # remove yamaha from the list
print(motorcycles)

# Removing element using pop() method: removes last element from the list
motorcycles.pop() # remove last element from the list
print(motorcycles)