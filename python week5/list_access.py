# Accessing list elements
# ACESSING ELEMENTS USING POSITIVE INDEXING
# # Accessing the first element of the list
list_of_integers = [1, 2, 3, 4, 5]
print(list_of_integers[0])  # Output: 1
# # Accessing the second element of the list
print(list_of_integers[1])  # Output: 2
# Accessing the third element of the list
print(list_of_integers[2])  # Output: 3
# Accessing the forth element of the list
print(list_of_integers[3])  # Output: 4   
# Accessing the fifth element of the list
print(list_of_integers[4])  # Output: 5
print()
print()

# ACCESSING ELEMENTS USING NEGATIVE INDEXING
# # Accessing the last element of the list
print(list_of_integers[-1])  # Output: 5
# # Accessing the second last element of the list
print(list_of_integers[-2])  # Output: 4
# # Accessing the third last element of the list
print(list_of_integers[-3])  # Output: 3
# # Accessing the forth last element of the list
print(list_of_integers[-4])  # Output: 2
# # Accessing the fifth last element of the list
print(list_of_integers[-5])  # Output: 1   

